abstract <- "The phage shock protein (Psp) stress-response system protects bacteria from
              envelope stress and stabilizes the cell membrane. Despite the prevalence of the key effector,
              PspA, and the functional Psp system, the various genomic contexts of Psp proteins, as well as
              their evolution across the kingdoms of life, have not yet been characterized. Recent work
              from our group suggests that the psp systems have evolved independently in distinct
              Gram-positive and Gram-negative bacterial clades to effect similar stress response functions.
              We developed a computational pipeline for comparative genomics and protein sequence-structure-function
              analyses to identify sequence homologs, phyletic patterns, domain architectures, gene neighborhoods,
              sequence conservation and evolution of the candidates across the tree of life. Using contextual
              information from conserved gene neighborhoods and their domain architectures, we delineated the phyletic
              patterns of all the Psp members. Next, we systematically identified all possible 'flavors' and genomic
              neighborhoods of the Psp systems. Finally, we have traced their evolution leading us to several interesting
              observations as to their occurrence and co-migration, suggesting their function and role in stress-response
              systems that are often lineage-specific. Conservation of the Psp systems across bacterial phyla emphasizes
              the established importance of this stress response system in prokaryotes, while the modularity in various
              lineages is indicative of adaptation to bacteria-specific cell-envelope structures, lifestyles, and
              adaptation strategies."